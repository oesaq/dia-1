let idade = Number(prompt('Insira sua idade: '));

switch (idade) {
    case 10:
        console.log('Você tem 10 anos');
        break;
    
    case 15:
        console.log('Você tem 15 anos');
        break;

    case 20:
        console.log('Você tem 20 anos');
        break;

    default:
        console.log('default');
        break;
}